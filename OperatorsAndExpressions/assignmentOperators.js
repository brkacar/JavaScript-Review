// Assignment operators are used to assign values to variables. 
// They can also perform an operation and assign the result to the variable in one step.
let x = 10; // Simple assignment operator: assigns the value 10 to x
console.log(x); // Output: 10

x += 5; // Addition assignment operator: equivalent to x = x + 5
console.log(x); // Output: 15

x -= 3; // Subtraction assignment operator: equivalent to x = x - 3
console.log(x); // Output: 12

x *= 2; // Multiplication assignment operator: equivalent to x = x * 2
console.log(x); // Output: 24

x /= 4; // Division assignment operator: equivalent to x = x / 4
console.log(x); // Output: 6

x %= 5; // Modulus assignment operator: equivalent to x = x % 5
console.log(x); // Output: 1 (6 divided by 5 leaves a remainder of 1)